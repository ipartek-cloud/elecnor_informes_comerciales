CREATE VIEW dbo.vwWEB_REG
AS
SELECT        CDCEN AS CodCentro, CDOFT AS CodOferta, ISNULL(NUMRE, 0) AS NumRegularizacion, DCOF AS DescripcionOferta, CDCLI AS CodCliente, LOCAL AS Localidad, 
                         PROOF AS CodProv, CDAC1 AS CodAct1, CDAC2 AS CodAct2, RPROF AS CodResponsable, CASE WHEN LEN(FECHAR) > 5 THEN CONVERT(datetime, RIGHT(FECHAR, 
                         6), 103) ELSE NULL END AS FAdjudicacion, IMPRE AS ImporteContratado, CAUS, CASE WHEN LEN(FECHAA) > 5 THEN CONVERT(datetime, RIGHT(FECHAA, 6), 103) 
                         ELSE NULL END AS FAlta, IMAOF AS ImporteAlta, CASE WHEN LEN(FECHPP) > 5 THEN CONVERT(datetime, RIGHT(FECHPP, 6), 103) ELSE NULL 
                         END AS FPresentacion, PREVE AS ImportePresentacion, dbo.fnActividadAgrupacion(CDAC1, CDAC2) AS Agrupacion, dbo.fnActividadDescripcion(CDAC1, CDAC2) 
                         AS Descripcion
FROM            dbo.Contratacion_Ofertas_Regularizaciones AS OfRe

GO
